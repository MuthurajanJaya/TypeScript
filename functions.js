"use strict";
// Named Function
function namedFunction(num1, num2) {
    return num1 + num2;
}
console.log(namedFunction(2, 3));
// Arrow Function
const arrowFunction = (num1, num2) => num1 - num2;
console.log(arrowFunction(2, 3));
// Anonymous Function
const anonymousFunction = function (num1, num2) {
    return num1 + num2;
};
